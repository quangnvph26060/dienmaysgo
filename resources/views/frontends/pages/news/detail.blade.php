                                        khác</span>
                                </li>
                                <li style="font-weight: 400" aria-level="1">
                                    <span style="font-weight: 400">Khi đổ nhiên liệu từ thùng phuy vào bình nhiên liệu
                                        của máy, cần đảm bảo không có bụi hoặc nước lẫn vào
                                        nhiên liệu để tránh các vấn đề về bơm phun nhiên liệu.
                                    </span>
                                </li>
                            </ul>
                            <h4 style="text-align: justify">
                                <span class="ez-toc-section" id="Kiem_tra_dau"></span><span style="font-weight: 400">Kiểm
                                    tra dầu</span><span class="ez-toc-section-end"></span>
                            </h4>
                            <p style="text-align: justify">
                                <span style="font-weight: 400">Sau khi chọn được nhiên liệu phù hợp, cách sử dụng máy
                                    phát điện chạy dầu tiếp theo đó là kiểm tra mức dầu hiện
                                    tại của máy</span>
                            </p>
                            <p style="text-align: justify">
                                <span style="font-weight: 400">Trước khi khởi động, luôn kiểm tra mức dầu động cơ của
                                    tổ máy phát điện trên bề mặt phẳng và đổ thêm nếu cần
                                    thiết. Nếu vận hành động cơ với dầu không đủ, có thể gây
                                    hỏng hóc. Tuy nhiên, đổ quá nhiều dầu cũng rất nguy hiểm
                                    vì tốc độ tăng đột ngột có thể xảy ra do quá trình đốt
                                    cháy của động cơ.</span>
                            </p>
                            <h4 style="text-align: justify">
                                <span class="ez-toc-section" id="Kiem_tra_to_nguon_cua_may"></span><span
                                    style="font-weight: 400">Kiểm tra tổ nguồn của máy</span><span
                                    class="ez-toc-section-end"></span>
                            </h4>
                            <p style="text-align: justify">
                                <span style="font-weight: 400">Bước kiểm tra cuối cùng trong cách sử dụng máy phát
                                    điện chạy dầu đó là kiểm tra tổ máy phát điện là một
                                    hoạt động quan trọng để đảm bảo an toàn tuyệt đối cho
                                    người sử dụng.</span>
                            </p>
                            <ul style="text-align: justify">
                                <li style="font-weight: 400" aria-level="1">
                                    <span style="font-weight: 400">Để khởi động động cơ, trước tiên cần đảm bảo các công
                                        tắc của thiết bị đang ở vị trí &#8220;TẮT&#8221;. Nếu
                                        không, việc áp dụng tải đột ngột khi khởi động có thể
                                        gây nguy hiểm.</span>
                                </li>
                                <li style="font-weight: 400" aria-level="1">
                                    <span style="font-weight: 400">Để tránh bị điện giật, tổ máy phát điện cần được đấu
                                        đất.
                                    </span>
                                </li>
                                <li style="font-weight: 400" aria-level="1">
                                    <span style="font-weight: 400">Công tắc chính luôn phải được giữ ở vị trí
                                        &#8220;ON&#8221; trong khi máy hoạt động.</span>
                                </li>
                            </ul>
