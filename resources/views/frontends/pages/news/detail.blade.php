                            <p style="text-align: justify">
                                <span style="font-weight: 400">Cách sử dụng máy phát điện chạy dầu tiết kiệm, hiệu quả
                                    nhất đó chính là chọn và sử dụng nguyên liệu phù hợp với
                                    máy để tránh lãng phí hay hư hỏng máy:</span>
                            </p>
                            <ul style="text-align: justify">
