                                    class="ez-toc-section-end"></span>
                            </h4>
                            <p style="text-align: justify">
                                <span style="font-weight: 400">Đây là hướng dẫn để khởi động bằng đề điện:
                                </span>
                            </p>
                            <ul style="text-align: justify">
                                <li style="font-weight: 400" aria-level="1">
                                    <span style="font-weight: 400">Chuẩn bị cho điện khởi động giống như máy giật.</span>
                                </li>
                                <li style="font-weight: 400" aria-level="1">
                                    <span style="font-weight: 400">Mở vòi nhiên liệu.</span>
                                </li>
                                <li style="font-weight: 400" aria-level="1">
                                    <span style="font-weight: 400">Đặt cần tốc độ động cơ ở vị trí
                                        &#8220;RUN&#8221;.</span>
                                </li>
                                <li style="font-weight: 400" aria-level="1">
                                    <span style="font-weight: 400">Vặn khóa khởi động theo chiều kim đồng hồ đến vị trí
                                        &#8220;START&#8221;.</span>
                                </li>
                                <li style="font-weight: 400" aria-level="1">
                                    <span style="font-weight: 400">Bỏ tay ra khỏi chìa khóa ngay khi động cơ khởi
                                        động.</span>
                                </li>
                                <li style="font-weight: 400" aria-level="1">
                                    <span style="font-weight: 400">Nếu động cơ không khởi động sau 10 giây, hãy đợi 15
                                        giây trước khi thử lại.</span>
                                </li>
                            </ul>
                            <h4 style="text-align: justify">
                                <span class="ez-toc-section" id="Luu_y_quan_trong"></span><span
