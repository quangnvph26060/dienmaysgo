                                    class="ez-toc-section-end"></span>
                            </h4>
                            <p style="text-align: justify">
                                <span style="font-weight: 400">Để khởi động máy phát điện bằng giật nổ, bạn thực hiện
                                    các bước sau đây:</span>
                            </p>
                            <ul style="text-align: justify">
                                <li style="font-weight: 400" aria-level="1">
                                    <span style="font-weight: 400">Mở khóa bình nhiên liệu.</span>
                                </li>
                                <li style="font-weight: 400" aria-level="1">
                                    <span style="font-weight: 400">Đặt cần tốc độ ở vị trí &#8220;run&#8221;.</span>
                                </li>
                                <li style="font-weight: 400" aria-level="1">
                                    <span style="font-weight: 400">Kéo tay cầm của bộ khởi động giật ra cho đến khi bạn
                                        cảm thấy nặng tay, sau đó đưa nó trở lại vị trí
                                        cũ.</span>
                                </li>
                                <li style="font-weight: 400" aria-level="1">
                                    <span style="font-weight: 400">Nhấn cần mở áp (giải nén) xuống, khi bộ khởi động
                                        được kéo, máy sẽ tự động trở lại.</span>
                                </li>
                                <li style="font-weight: 400" aria-level="1">
                                    <span style="font-weight: 400">Dùng hai tay giật nhanh tay cầm của bộ khởi động để
                                        đảm bảo an toàn</span>
                                </li>
                            </ul>
                            <h4 style="text-align: justify">
                                <span class="ez-toc-section" id="Khoi_dong_may_phat_dien_bang_de_dien"></span><span
