                            <p style="text-align: justify">
                                <span style="font-weight: 400">Sau khi sử dụng máy, bạn cũng nên thận trọng, làm theo
                                    những bước sau đây để giữ an toàn cho bản thân và duy
                                    trì tuổi thọ của máy:</span>
                            </p>
