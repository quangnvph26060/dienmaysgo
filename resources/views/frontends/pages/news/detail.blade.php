                                    class="ez-toc-section-end"></span>
                            </h4>
                            <p style="text-align: justify">
                                <span style="font-weight: 400">Để máy luôn giữ được độ bền và giảm thiểu chi phí phát
                                    sinh để bảo dưỡng máy, bạn nên lưu ý những bước sau
                                    đây:</span>
                            </p>
                            <ul style="text-align: justify">
                                <li style="font-weight: 400" aria-level="1">
                                    <span style="font-weight: 400">Hãy kiểm tra mức điện phân của ắc quy hàng
                                        tháng.</span>
                                </li>
                                <li style="font-weight: 400" aria-level="1">
                                    <span style="font-weight: 400">Nếu mức chất điện phân giảm xuống giới hạn dưới, hãy
                                        đổ đầy nước cất đến giới hạn trên.</span>
                                </li>
                            </ul>
                            <h3 style="text-align: justify">
